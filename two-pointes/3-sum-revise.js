function tripletSum(nums) {
  nums.sort((a, b) => a - b);
  let result = [];

  for (let i = 0; i < nums.length - 2; i++) {
    if (i > 0 && nums[i] === nums[i - 1]) continue;
    let left = i + 1;
    let right = nums.length - 1;
    while (left < right) {
      const tripleSum = nums[i] + nums[left] + nums[right];
      if (tripleSum === 0) {
        result.push([nums[i], nums[left], nums[right]]);
        if (left < right && nums[left] === nums[left + 1]) {
          left++;
        }
        if (left < right && nums[right] === nums[right - 1]) {
          right++;
        }
        left++;
        right--;
      } else if (tripleSum < 0) {
        left++;
      } else if (tripleSum > 0) {
        right--;
      }
    }
  }
  return result;
}

const nums = [-1, 0, 1, 2, -1, -1]; //[[-1,-1,2],[-1,0,1]]
console.log("three sum", tripletSum(nums));
